# Run these commands in your terminal to push to GitHub:

# 1. Initialize git (if not already initialized)
git init

# 2. Add all files
git add .

# 3. Commit the changes
git commit -m "Refactor: Server components, folder restructuring, reusable components, input contrast fixes"

# 4. Add remote repository
git remote add origin https://github.com/Dazycokar/react-demo-app-interview.git

# 5. Create and switch to new branch with prefix
git checkout -b blackboxai/improvements

# 6. Push to GitHub
git push -u origin blackboxai/improvements
